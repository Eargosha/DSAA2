    /// @brief –ò–∑–≤–ª–µ–∫–∞–µ—Ç –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π —ç–ª–µ–º–µ–Ω—Ç –∏–∑ –∫—É—á–∏
    /// –°–ª–æ–∂–Ω–æ—Å—Ç—å –≤ —Å—Ä–µ–¥–Ω–µ–º –∏ —Ö—É–¥—à–µ–º –∏ –ª—É—á—à–µ–º - O(1)
    /// @return –ú–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π —ç–ª–µ–º–µ–Ω—Ç –∏–∑ –∫—É—á–∏
    T extractMax()
    {
        if (isEmpty())
        {
            throw runtime_error("–ö—É—á–∞ –ø—É—Å—Ç–∞");
        }

        T max = heap[0];
        heap[0] = heap.back();
        heap.pop_back();
        heapifyDown(0);

        return max;
    }

